Math.abs(10); //Converte o sinal do número pra positivo
Math.abs(-10); //Converte o sinal do número pra positivo
Math.ceil(1.1) //arredonda o número pra cima
Math.ceil(-1.1) //arredonda o número pra cima em direção a zero quando negativo
Math.floor(9.9) //arredonda o número pra baixo
Math.floor(-9.9) //arredonda o número pra baixo se afastando do zero quando negativo
Math.round(4.5) //arredonda o número pra cima se a parte decimal for de 5 a 9 e para baixo se for de 0 a 4
Math.round(-4.5) //arredonda o número pra cima se a parte decimal for de 5 a 9 e para baixo se for de 0 a 4
Math.sign(5) //Retorna 1 se o número for positivo e -1 se for negativo
Math.sign(-5) //Retorna 1 se o número for positivo e -1 se for negativo
Math.trunc(2.3) //Elimina a parte decimal do número tornando-o um inteiro
Math.trunc(-2.3) //Elimina a parte decimal do número tornando-o um inteiro